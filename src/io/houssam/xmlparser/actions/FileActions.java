package io.houssam.xmlparser.actions;

public class FileActions {
	
	public FileActions() {
	
	}
	
	public void newFile() {
		System.out.println("New File...");
	}
	
	public void open() {
		System.out.println("Open File...");
	}
	
	public void save() {
		System.out.println("Save File...");
	}
	
	public void saveAs() {
		System.out.println("Save File As...");
	}
	
	public void exit() {
		System.exit(0);
	}
}
